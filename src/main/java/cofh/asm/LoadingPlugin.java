	public static void attemptClassLoad(String className, String failMessage) {

		try {
			Class.forName(className, false, LoadingPlugin.class.getClassLoader());
			transformersList.add(className);
		} catch (Throwable e) {
			FMLLog.warning(failMessage);
		}
	}

