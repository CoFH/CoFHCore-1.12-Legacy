		obfuscated = obf;
		if (!obfuscated) {
			try {
				CoFHAccessTransformer.initForDeobf();
			} catch (IOException e) {
			}
		}
		currentMcVersion = (String) FMLInjectionData.data()[4];
		minecraftDir = (File) FMLInjectionData.data()[6];
		loader = Launch.classLoader;
